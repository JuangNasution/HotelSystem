    private void jButton_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_addActionPerformed
        int id = Integer.parseInt(jTextField_id.getText());
        String fname = jTextField_fname.getText();
        String lname = jTextField_lname.getText();
        String phone = jTextField_phone.getText();
        String email = jTextField_email.getText();
        if(fname.trim().equals("")||lname.trim().equals("")||phone.trim().equals("")||email.trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "required field");
        }
        else{
            if(client.addClient(id,fname,lname,phone,email))
                JOptionPane.showMessageDialog(rootPane, "Client Added succesfully","add client",JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog(rootPane, "client not added","add client error",JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_jButton_addActionPerformed

