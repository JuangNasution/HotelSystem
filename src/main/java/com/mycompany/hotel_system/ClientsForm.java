    private void jButton_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_removeActionPerformed
        if(jTextField_id.getText().equals(""))
            JOptionPane.showMessageDialog(rootPane, "Select id to remove");
        else{
            int id = Integer.parseInt(jTextField_id.getText());
            if(client.removeClient(id))
                JOptionPane.showMessageDialog(rootPane, "Client removed succesfully","remove client",JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog(rootPane, "ID client not match","remove client error",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton_removeActionPerformed

