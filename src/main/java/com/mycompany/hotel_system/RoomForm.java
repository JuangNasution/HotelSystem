    private void jComboBox_typeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox_typeActionPerformed
        String price="";
        if(!"select type".equals(String.valueOf(jComboBox_type.getSelectedItem())))
            jComboBox_type.removeItem("select type");
        switch(jComboBox_type.getSelectedIndex()){
            case 0:
                price = "100";
                break;
            case 1:
                price = "200";
                break;
            case 2:
                price = "300";
                break;
            case 3:
                price="400";
                break;
            case 4:
                price="500";
                break;
        }
        
        jTextField_price.setText(price);
    }//GEN-LAST:event_jComboBox_typeActionPerformed

