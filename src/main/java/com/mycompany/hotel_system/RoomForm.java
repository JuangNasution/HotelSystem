    private void jButton_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_addActionPerformed
        int number = Integer.parseInt(jTextField_number.getText());
        String type = String.valueOf(jComboBox_type.getSelectedItem());
        String phone = jTextField_phone.getText();
        String reserved = "";
        if(jRadioButton_no.isSelected())
            reserved = "NO";
        else
            reserved = "YES";
        if(String.valueOf(number).trim().equals("")||type.trim().equals("")||phone.trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "required field");
        }
        else{
            if(room.addRoom(number, type, phone, reserved))
            JOptionPane.showMessageDialog(rootPane, "Room Added succesfully","add room",JOptionPane.INFORMATION_MESSAGE);
            else
            JOptionPane.showMessageDialog(rootPane, "Room not added","add room error",JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton_addActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel dm = (DefaultTableModel)jTable1.getModel();
        int rIndex = jTable1.getSelectedRow();
        jTextField_number.setEnabled(false);
        jTextField_number.setText(dm.getValueAt(rIndex, 0).toString());
        jComboBox_type.setSelectedItem(dm.getValueAt(rIndex, 1).toString());
        jTextField_phone.setText(dm.getValueAt(rIndex, 2).toString());
        if(dm.getValueAt(rIndex, 3).equals("YES"))
            jRadioButton_yes.setSelected(true);
        else
            jRadioButton_no.setSelected(true);
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_removeActionPerformed
        if(jTextField_number.getText().equals(""))
            JOptionPane.showMessageDialog(rootPane, "Select number to remove");
        else{
            int number = Integer.parseInt(jTextField_number.getText());
            if(room.removeRoom(number))
                JOptionPane.showMessageDialog(rootPane, "room removed succesfully","remove client",JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog(rootPane, "number room not match","remove client error",JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton_removeActionPerformed

    private void jButton_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_editActionPerformed
        String type = String.valueOf(jComboBox_type.getSelectedItem());
        String phone = jTextField_phone.getText();
        String reserved = "";
        if(jRadioButton_no.isSelected())
            reserved = "NO";
        else
            reserved = "YES";
        int number = 0;
        if(type.trim().equals("")|phone.trim().equals("")||reserved.trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "required field");
        }
        else{
            number = Integer.parseInt(jTextField_number.getText());
            if(room.editRoom(number,type,phone,reserved))
            JOptionPane.showMessageDialog(rootPane, "Room edited succesfully","edit room",JOptionPane.INFORMATION_MESSAGE);
            else
            JOptionPane.showMessageDialog(rootPane, "Room not edited","edit room error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton_editActionPerformed

